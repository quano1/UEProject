// if set to 1, then we do not default-initialize our new mesh object parameters based on the Modeling Components Settings (the Modeling Tools' Project Settings)
static TAutoConsoleVariable<bool> CVarConstructMeshObjectsWithoutModelingComponentSettings(
	TEXT("modeling.CreateMesh.IgnoreProjectSettings"),
	false,
	TEXT("If enabled, do not use the preferences set in Modeling Tools' Project Settings when constructing new mesh objects"));

FTLLRN_CreateMeshObjectParams::FTLLRN_CreateMeshObjectParams(bool bConstructWithDefaultModelingComponentSettings)
{
	if (bConstructWithDefaultModelingComponentSettings && 
		!CVarConstructMeshObjectsWithoutModelingComponentSettings.GetValueOnGameThread())
	{
		UTLLRN_ModelingComponentsSettings::ApplyDefaultsToTLLRN_CreateMeshObjectParams(*this);
	}
}

void FTLLRN_CreateMeshObjectParams::SetMesh(FMeshDescription&& MeshDescriptionIn)
{
	MeshDescription = MoveTemp(MeshDescriptionIn);
	MeshType = ETLLRN_CreateMeshObjectSourceMeshType::MeshDescription;
}

void FTLLRN_CreateMeshObjectParams::SetMesh(const UE::Geometry::FDynamicMesh3* DynamicMeshIn)
{
	DynamicMesh = *DynamicMeshIn;
	MeshType = ETLLRN_CreateMeshObjectSourceMeshType::DynamicMesh;
	if(DynamicMesh)
	{ 
		UE::Geometry::FNonManifoldMappingSupport::RemoveAllNonManifoldMappingData(*DynamicMesh);
	}
}

void FTLLRN_CreateMeshObjectParams::SetMesh(UE::Geometry::FDynamicMesh3&& DynamicMeshIn)
{
	DynamicMesh = MoveTemp(DynamicMeshIn);
	MeshType = ETLLRN_CreateMeshObjectSourceMeshType::DynamicMesh;
	if (DynamicMesh)
	{
		UE::Geometry::FNonManifoldMappingSupport::RemoveAllNonManifoldMappingData(*DynamicMesh);
	}
}







FTLLRN_CreateMeshObjectResult UE::Modeling::CreateMeshObject(UInteractiveToolManager* ToolManager, FTLLRN_CreateMeshObjectParams&& CreateMeshParams)
{
	if (ensure(ToolManager))
	{
		UTLLRN_ModelingObjectsCreationAPI* UseAPI = ToolManager->GetContextObjectStore()->FindContext<UTLLRN_ModelingObjectsCreationAPI>();
		if (UseAPI)
		{
			if (UseAPI->HasMoveVariants())
			{
				return UseAPI->CreateMeshObject(MoveTemp(CreateMeshParams));
			}
			else
			{
				return UseAPI->CreateMeshObject(CreateMeshParams);
			}
		}
	}
	return FTLLRN_CreateMeshObjectResult{ ETLLRN_CreateModelingObjectResult::Failed_NoAPIFound };
}





